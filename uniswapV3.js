async function fetchData(address) {
    const query = `
    query {
    positions(
        where: {owner: "${address}"}
        ) {
            id
            depositedToken0
            depositedToken1
            withdrawnToken0
            withdrawnToken1
            collectedFeesToken0
            collectedFeesToken1
            pool {
                id
                token0 {
                    symbol
                }
                token1 {
                    symbol
                }
                liquidityProviderCount
            }
        }
    }
    `;
    try {
        const response = await axios.post(endpoint, {
            query,
        });
        const positions = response.data.data.positions;

        console.log(`Total Pools ${positions.length}`)
        console.log()

        for (let position of positions) {
            console.log(`Pool Name: ${position.pool.token0.symbol}-${position.pool.token1.symbol}, Pool Address: ${position.pool.id}`)
            console.log(`Rewarded ${position.pool.token0.symbol}: ${position.collectedFeesToken0}, Rewarded ${position.pool.token1.symbol}: ${position.collectedFeesToken0}`)
            console.log(`Deposited ${position.pool.token0.symbol}: ${position.depositedToken0}, Deposited ${position.pool.token1.symbol}: ${position.depositedToken1}`)
            console.log(`Withdrawn ${position.pool.token0.symbol}: ${position.withdrawnToken0}, Withdrawn ${position.pool.token1.symbol}: ${position.withdrawnToken1}`)
            console.log()
        }
    } catch (error) {
        console.error('Error fetching data:', error);
    }
}

